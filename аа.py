    # Метод .strip() видаляє зайві пробіли на початку та в кінці.
    # Метод .capitalize() робить першу літеру великою, щоб порівняння 
    # було менш чутливим до регістру (наприклад, "купую" перетворюється на "Купую").
    відповідь = input("Купи слона! ").strip().capitalize()
